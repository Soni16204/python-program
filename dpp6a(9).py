#What is a method in a class?
#○ Describe what a method is and how it differs from a function.
#In a class, a method is a function that is defined inside the class and is associated with an instance of that class. Methods are used to define behaviors that objects of the class can perform. They can access and modify the object’s attributes, allowing objects to interact with each other and maintain their own internal state.

#Key Characteristics of a Method:
#Defined Within a Class: A method is written inside a class and typically operates on the data within the object (instance).
#First Parameter is self: The first parameter of a method is always self, which represents the instance calling the method. T
# his allows the method to access the object’s attributes and other methods.
#Encapsulation: Methods enable encapsulation by controlling how the object’s data is accessed and modified, hiding implementation details from the outside.
