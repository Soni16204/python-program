#What is a class in Python?
#â—‹ Explain what a class represents in object-oriented programming.
#In Python, a class is a blueprint for creating objects. It defines a set of attributes and methods that the objects created from the class will have. 
# In object-oriented programming (OOP), a class represents a concept, template, or "type" that groups related data and behaviors together.

#Here's how classes work in OOP and Python:

#Structure: A class is a structure that defines the properties (attributes) and behaviors (methods) that an object of that class will have.
#  Attributes are typically variables that hold data, and methods are functions that define what the object can do.

#Blueprint for Objects: An object is an instance of a class. Once a class is defined, it can be used to create multiple objects (instances), 
# each with the same set of attributes and methods but with potentially different values for the attributes.

#Encapsulation: Classes allow for encapsulation, which means bundling the data and methods that operate on the data within one unit (the class).
#  This helps organize code and keeps related functionality together.

#Example: Think of a Car class. The class might define attributes like color, make, and model, and methods like start() and stop().
#  When you create an object from this class (e.g., my_car = Car("red", "Toyota", "Camry")), my_car will have the properties and methods defined in Car, tailored to that specific instance.