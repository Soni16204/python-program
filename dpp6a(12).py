#Can you create a class without any attributes or methods?
#○ Discuss whether it’s possible and provide a brief code example
#Yes, it is possible to create a class in Python without any attributes or methods. Such a class is sometimes called an "empty class." Although it doesn't do anything on its own, it can serve as a placeholder or a base class for other classes that may add functionality later.

#To create an empty class, you use the class keyword followed by the class name and the pass statement, which acts as a placeholder for the class body.

#Here's an example:

#python
#Copy code
class EmptyClass:
    pass
#Explanation
#The pass statement is used to indicate that the class has no content. Without it, Python would raise a syntax error because the class would have an empty body.






